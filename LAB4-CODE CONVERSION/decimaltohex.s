	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10000000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	MOV R1,#0X01
	MOV R11,#0X0A
	LDR R0,=SRC1
	LDR R2,[R0]
	LDR R7,=DST
	
UP
	AND R3,R2,#0X0F
	LSR R2,#4
	MUL R3,R1
	MUL R1,R11
	ADD R9,R3
	CMP R2,#0
	BNE UP
	STR R9,[R7]
STOP
	B STOP
SRC1 DCD 0X0057005
	AREA data,DATA,READWRITE
DST DCD 0
	END
	